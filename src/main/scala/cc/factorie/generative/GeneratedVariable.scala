  type ChildType = C
  def child = _1
  def parents: Seq[GeneratedVar] = Nil
  def score(s:Statistics) = logpr(s.asInstanceOf[StatisticsType]) // Can't define score earlier because inner class Factor.Statistics not defined until here
}

